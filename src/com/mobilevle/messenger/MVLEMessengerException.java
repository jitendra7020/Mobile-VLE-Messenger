public class MVLEMessengerException extends Exception {

    public MVLEMessengerException() {
        super();
    }

    public MVLEMessengerException(String message) {
        super(message);
    }

    public MVLEMessengerException(String message, Throwable cause) {
        super(message, cause);
    }

    public MVLEMessengerException(Throwable cause) {
        super(cause);
    }
}
